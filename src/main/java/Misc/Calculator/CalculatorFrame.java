    private void completeCalculation() {
        switch (previousOperation) {
            case '*':
                currentNumber = previousNumber * currentNumber;
                break;
            case '/':
                currentNumber = previousNumber / currentNumber;
                break;
            case '+':
                currentNumber = previousNumber + currentNumber;
                break;
            case '-':
                currentNumber = previousNumber - currentNumber;
                break;
        }

        previousOperation = currentOperation;
    }

