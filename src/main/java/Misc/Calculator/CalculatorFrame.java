    private void multiplyButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_multiplyButActionPerformed
        previousNumber = currentNumber;
        currentNumber = Double.parseDouble(currentDisplay);
        currentOperation = '*';

        if (firstNum) {
            firstNum = false;
            currentDisplay = "";
            display.setText("");
        } else {
            //do previous calc, set current operation to *
            completeCalculation();
            //reset the saved num string
            currentDisplay = "";
            display.setText("" + currentNumber);
        }
    }//GEN-LAST:event_multiplyButActionPerformed

